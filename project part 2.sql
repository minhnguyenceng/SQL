SELECT p.PAS_NAME,t.TIC_SEAT AS "Seat Number"
FROM passenger p
JOIN ticket t ON t.TIC_ITINERARY_NO = p.PAS_ITINERARY_NO
WHERE t.TIC_FLIGHT_NO = '101' AND t.TIC_FLIGHT_DATE = '15-JUL-17'
ORDER BY PAS_NAME

8.

SELECT DEP_FLIGHT_NO AS "Flight Number", DEP_DEP_DATE  AS "Date",COUNT(*) AS "Number of Passengers"
from departures d
JOIN ticket t ON t.TIC_FLIGHT_NO = d.DEP_FLIGHT_NO
JOIN passenger p ON p.PAS_ITINERARY_NO = t.TIC_ITINERARY_NO
GROUP BY DEP_FLIGHT_NO, DEP_DEP_DATE
HAVING COUNT(*) > 5;

