SELECT PIL_PILOT_ID, PIL_PILOTNAME
FROM pilots
MINUS
SELECT DEP_PILOT_ID, PIL_PILOTNAME
FROM departures
JOIN pilots ON DEP_PILOT_ID = PIL_PILOT_ID;

5.

SELECT PIL_PILOT_ID, PIL_PILOTNAME
FROM pilots
WHERE PIL_PILOT_ID NOT IN (SELECT DEP_PILOT_ID FROM departures)
ORDER BY PIL_PILOT_ID;

6.

SELECT p.PIL_PILOT_ID, p.PIL_PILOTNAME
FROM pilots p
LEFT JOIN departures d
ON p.PIL_PILOT_ID = d.DEP_PILOT_ID
WHERE d.DEP_PILOT_ID IS NULL
ORDER BY p.PIL_PILOT_ID;

