SELECT f.FL_FLIGHT_NO, f.FL_ORIG, f.FL_DEST, a.AIR_HUB_AIRLINE
FROM flight f
JOIN airport a ON f.FL_ORIG = a.AIR_CODE
WHERE a.AIR_HUB_AIRLINE = 'American' OR a.AIR_HUB_AIRLINE IS NULL
ORDER BY f.FL_FLIGHT_NO;

2.

SELECT f.FL_FLIGHT_NO, f.FL_ORIG, f.FL_DEST, a.AIR_HUB_AIRLINE
FROM flight f
JOIN airport a ON f.FL_ORIG = a.AIR_CODE
WHERE a.AIR_HUB_AIRLINE IS NULL
UNION
SELECT f.FL_FLIGHT_NO, f.FL_ORIG, f.FL_DEST, a.AIR_HUB_AIRLINE
FROM flight f
JOIN airport a ON f.FL_ORIG = a.AIR_CODE
WHERE a.AIR_HUB_AIRLINE = 'American'
ORDER BY FL_FLIGHT_NO;

3.

SELECT d.DEP_FLIGHT_NO, d.DEP_DEP_DATE, e.EQ_EQUIP_TYPE
FROM departures d
JOIN equip_type e ON d.DEP_EQUIP_NO = e.EQ_EQUIP_NO
WHERE e.EQ_EQUIP_TYPE = 'CONCORDE'
ORDER BY d.DEP_DEP_DATE, d.DEP_FLIGHT_NO;

