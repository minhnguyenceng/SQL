SELECT f.FL_FLIGHT_NO, f.FL_ORIG, f.FL_DEST 
FROM flight f
JOIN reservation r ON r.RES_FLIGHT_NO = f.FL_FLIGHT_NO
WHERE r.RES_NAME = 'Andy Anderson'
ORDER BY f.FL_FLIGHT_NO

10.

SELECT a1.AIR_LOCATION AS "Departs From", a2.AIR_LOCATION AS "Arrives at", MIN(FL_FARE) AS "Minimum Fare"
FROM flight
JOIN airport a1 ON flight.FL_ORIG = a1.AIR_CODE
JOIN airport a2 ON flight.FL_DEST = a2.AIR_CODE
GROUP BY a1.AIR_LOCATION, a2.AIR_LOCATION
